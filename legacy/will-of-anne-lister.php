            <h1 class="text-center">The will of Anne Lister</h1>
            <img class="letter-banner" src="../images/legacy/index-legacy-one.jpg" alt="">

            <p>Anne Lister was a prominent landowner from Halifax who
                had relationships with many women, before meeting
                Ann Walker. The two became lovers and lived together at
                Shibden Hall, Lister’s home. Lister died from a fever when
                the two were travelling together in Georgia, and in her
                will bequeathed her substantial estate to her ‘friend’ Ann
                Walker. In an unusual, and perhaps rather controlling, twist,
                she stipulated that if Walker should marry she would be
                disinherited "… as if the said Ann Walker should have then
                departed this life'.</p>
                <p>Walker was not to stay at Shibden Hall for long. Two years
                after Lister’s death, she was declared of “unsound mind”
                and removed to an asylum in York.</p>

