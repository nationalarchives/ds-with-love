            <h2>Why is this in The National Archives?</h2>
            <p>Although it has never been illegal for women to have relationships with
                other women, in Anne Lister's lifetime it was not socially acceptable.
                Evidence of women's relationships with other women can therefore be hard
                to find in archive material before the 20th century. The National Archives
                holds a substantial collection of wills, and here there are occasional traces
                of women's relationships with other women. Lister's will is a unique example
                that shows real insight into the couple’s relationship.</p>
