                    <p>Monday Oct 21st 1805</p>
                    <p>At day light saw the Enemys combined fleet from East
                        to ESE bore away made the signal for order of sailing and
                        to prepare for Battle the Enemy with their heads to the
                        Southward, at 7 the Enemy wearing in succession, may the
                        Great God whom I worship grant</p>
                    <p>[OVERLEAF]</p>
                    <p>to my Country and for the benefit of Europe in General
                        a great and Glorious victory, and may no misconduct in
                        any one tarnish it, and may humanity after victory be
                        the predominant feature in the British fleet, nor myself
                        individually I commit my Life to him who made me,</p>
                    <p>[OVERLEAF]</p>
                    <p>and may his blessing light upon my endeavours for serving
                        my country faithfully to Him I resign myself and the just
                        cause which is entrusted to me to Defend- amen,
                        amen, amen</p>
                    <p>[OVERLEAF]</p>
                    <p> October the Twenty First one thousand Eight hundred and
                        five then in sight of the combined fleets of France and Spain
                        distant about Ten miles.</p>
                    <p>Whereas the Eminent services of Emma Hamilton widow of
                        the Right Honorable Sir William Hamilton have been of the
                        very greatest service to our King + Country, to my</p>
                    <p>[OVERLEAF]</p>
                    <p>knowledge without ever receiving any reward from either
                        our King or Country, first that she obtainâ€™d the King of Spain
                        letter in 1796 to His Brother the King of Naples acquainting
                        him of his intention to Declare war against England from
