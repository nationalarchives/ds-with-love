                        Jervis to strike a stroke of oppor-</p>
                    <p>[OVERLEAF]</p>
                    <p>tunity offered against either the arsenals of Spain or her
                        fleets-that neither of these was done is not the fault of
                        Lady Hamilton the opportunity might have been offered,
                        secondly the British fleet under my Command could never
                        have returnâ€™d the Second time to Egypt had not Lady
                        Hamiltons influence with the Queen of Naples</p>
                    <p>[OVERLEAF]</p>
                    <p>caused Letters to be wrote to the Governor of Syracuse
                        that he was to encourage the fleet being supplied with
                        everything should they put into any port in Sicily. We put
                        into Syracuse and received every supply went to Egypt and
                        destroyed the French fleet. Could I have rewarded these
                        services I would not now call upon my country but as that</p>
                    <p>[OVERLEAF]</p>
                    <p>has not been in my power I Leave Emma Lady Hamilton
                        therefore a Legacy to my King and Country that they will
                        give her an ample provision to maintain her Rank in Life. I
