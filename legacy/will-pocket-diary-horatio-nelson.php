            <h1 class="text-center">The will and the pocket diary of Horatio Nelson</h1>
            <img class="letter-banner" src="../images/legacy/index-legacy-two.jpg" alt="">

            <p>Before his death at Trafalgar, Admiral Lord Horatio Nelson
                wrote in his diary, "Drove from dear Merton where I left
                all which I hold dear in this World to â€¦ serve my King and
                Country". By "all which I hold dear", Nelson meant
                his mistress, Emma Hamilton, and their daughter Horatia,
                despite society's disapproval of the affair. He requested
                the state "give her an ample provision to maintain her
                Rank in Life" [second page, from six lines down] should he
                die in battle.</p>
            <p>Following Nelson's lavish state funeral, his wishes were
                disregarded, and a shunned Emma died destitute in
                France in 1815.</p>

