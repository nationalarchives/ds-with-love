                    <h4>The will of Horatio Nelson</h4>
                    <p>This is the last will and testament of me Horatio Viscount
                        Nelson of the Nile and of Burnham Thorpe in the county of
                        Norfolk and United Kingdom of Great Britain and Ireland
                        and Duke of Bronte in the kingdom of Farther Sicily.</p>
                    <p>First in the event that I shall die in England I direct my
                        executors hereinafter named (unless His Majesty shall
                        signify it to be his pleasure that my body shall be interred
                        elsewhere) to cause my body to be interred in the parish
                        church of Burnham Thorpe in the county of Norfolk
                        aforesaid near the remains of my deceased father and
                        mother and in as private a manner as may be and I direct
                        that the sum of one hundred pounds shall be divided
                        amongst the poor of the several parishes of Burnham
                        Thorpe Sutton and Norton all in the county of Norfolk
                        that is to say one third part to the poor of each of the said
                        parishes the same to be distributed at the discretion of the
                        respective curates or officiating ministers of those parishes
                        and in such manner and proportions and to such objects
                        as they respectively shall think fit and I give and bequeath
                        to Emma Lady Hamilton widow of the right honourable Sir
                        William Hamilton knight of the most honourable order of
                        the Bath my diamond star as a token of my friendship and
                        regard I likewise give and bequeath to the said Emma Lady
                        Hamilton the silver cup marked E.H which she presented
                        to me I give and bequeath to my brother the reverend
                        William Nelson doctor in divinity the gold box presented
                        to me by the city of London also I give and bequeath to the
                        said William Nelson the gold sword presented to me by
                        the captains who fought with me at the battle of the Nile
                        â€“ also I give and bequeath to my sister Catherine Matcham
                        the sword presented to me by the city of London also I
                        give and bequeath to my sister Susannah Bolton the silver
                        cup presented to me by the Turkey company also I give
                        and bequeath to Alexander Davison of St Jamesâ€™s Square in
                        the county of Middlesex esquire my Turkish Gun scimitar
                        + Canteen also I give and bequeath to my late captain and
                        worthy friend Captain Hardy all my telescopes and seaglasses
                        and one hundred pounds in money to be paid three
                        months after my death.</p>
                    <p>Horatio Legatt, William Slaughter junior, Henry Fletcher</p>
                        <p>Nelson+Bronte</p>
