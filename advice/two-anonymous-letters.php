            <h2>Why is this in The National Archives?</h2>
            <p>The letters are included in the State Papers collection, and may originally
                have been part of a family or private collection that has made its way into
                the archive as a result of a court case, intelligence gathering, or perhaps the
                matter was considered of interest to government for other reasons. Private
                papers also end up in the State Papers when they have been confiscated
                from individuals under investigation for treason. Unfortunately there is little
                evidence in the letters to suggest why they are part of this collection, but
                their writer has captured the intense and anguished feelings of two unknown
                lovers and the “great uneasiness” he himself feels at the prospect of his
                friend making an ill-advised marriage with another woman.</p>

