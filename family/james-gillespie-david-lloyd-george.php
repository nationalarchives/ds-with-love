            <h3>Read the transcript</h3>
            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>3 Weston Square<br>
                        Cadoxton<br>
                        Nr Barry Dock</p>
                    <p>October 24th 1919</p>
                    <p>To</p>
                    <p>Honourable D Lloyd George,</p>
                    <p>Premier</p>
                    <p>Honoured Sir</p>
                    <p>Pardon me for taking up a few seconds of your precious
                        time reading my humble letter. Sir I am a native of Jamaica
                        British West Indies. Been a seaman by profession, sailing
                        out of the Port of Barry Dock (from/since 1896 till 1917
                        when I stopped ... to go in Government work from the
                        29th of September 1917 to the 1st of June 1918. The work
                        was finished (Granaries) I started a little business in the
                        refreshment department (fish friers) until the last racial
                        riot 12 of June 1919 when my home was destroyed by the
                        rioters. I applied for repatriation for myself and family
                        several times, to the Home Office, Colonial Office and
                        the West Indian Committee, I filled a form in likewise a
                        letter from my creditors giving me permission to leave
                        the country. Its 4 months since I apply for aid to left the
                        country. I received a letter on the 21st inst. informing me
                        that I refused this offer unless 'certain concessions were
                        made'. The interpretation of the application is I am willing
                        to leave the country at once with my wife and child (not
                        without) without any compensation although I am ruin
                        in monetary matters to the amount of 227£. I have to
                        sacrifice what little I have left in furniture with the 26/ a
                        week out of</p>
                    <p>[OVERLEAF]</p>
                    <p>work donation to keep the 'wolf from the door'. I am
                        begging honoured sir as the leader of the British Empire to
                        give me and family passage to Jamaica. I am not asking sir
                        for any compensation, would pay my own fare if I was in
                        a position to do so but what little I have its just enough to
                        start with in Jamaica I don’t wants to take my family out
                        there to me a burden on the country. Trusting sir you will
                        take compassion on one of Britain’s son even if his colour
                        is black.</p>
                    <p>I remains sire, your humble servant.</p>
                    <p>James Gillespie</p>
