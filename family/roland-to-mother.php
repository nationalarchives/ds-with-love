                    <p>24 March 1951</p>
                    <p>Dear Mummy,</p>
                    <p>Thank you for the lovely post card you sent me. We are
                        playing at a school called Highbury. Please may I have an
                        aeroplane and a puzzle book and please put enough stamps
                        on the parcel. I came 6th again this fortnight. It was Good
                        Friday yesterday and we had lovely service. I am in the choir
                        and I am the leader. I am very sorry I have not more news.</p>
                    <p>With love from Roland.</p>
                </div>
