            <h2>Why is this in The National Archives?</h2>
            <p>Most divorce files held at The National Archives are weeded before they are
                transferred from the courts, so few contain such personal and handwritten
                records as this child’s letter. However, some files were selected to be
                transferred in their entirety in order to illustrate the changing nature of
                divorce, and perhaps love.</p>
