            <h3>Read the transcript</h3>
            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>Master Culpeper,</p>
                    <p>I heartily recommend me unto you, praying you to send me
                        word how that you do. It was showed me that you was sick,
                        the which thing troubled me very much till such time that
                        I hear from you praying you to send me word how that you
                        do, for I never longed so much for a thing as I do to see
                        you and to speak with you, the which I trust shall be
                        shortly now.</p>
                    <p>That which doth comfortly me very much when I think of it,
                        and when I think again that you shall depart from me again
                        it makes my heart die to think what fortune I have that I
                        cannot be always in your company.</p>
                    <p>Yet my trust is always in you that you will be as you have
                        promised me, and in that hope I trust upon still, praying
                        you that you will come when my Lady Rochford is here for
                        then I shall be best at leisure to be at your commandment,
                        thanking you for that you have promised me to be so good
                        unto that poor fellow my man which is one of the griefs
                        that I do feel to depart from him for then I do know no one
                        that I dare trust to send to you, and therefore I pray you
                        take him to be with you that I may sometime hear from
                        you one thing.</p>
                    <p>I pray you to give me a horse for my man for I had much
                        ado to get one and therefore I pray send me one by him
                        and in so doing I am as I said afor, and thus I take my leave
                        of you, trusting to see you shortly again and I would you
                        was with me now that you might see what pain I take in
                        writing to you.</p>
                    <p>Yours as long as life endures</p>
                    <p>Katheryn.</p>
                    <p>[She added a postscript:]</p>
                    <p>One thing I had forgotten and that is to instruct my man
                        to tarry here with me still for he says whatsomever you bid
                        him he will do it.</p>
