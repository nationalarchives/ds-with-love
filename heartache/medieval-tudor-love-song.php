            <h1 class="text-center">Medieval or Tudor love song</h1>
            <img class="letter-banner" src="../images/heartache/index-heartache-two.jpg" alt="">
            <blockquote>
                <p class="mb-0">"Alone I live, alone.<br>
                    And sore I sigh for one."</p>
            </blockquote>
            <p>These are words that may have been sung at the court of
                Henry VIII – the refrain "Alone I live, alone" also appears in
                a songbook owned by the King.</p>
            <p>The poem bears signs of the writer’s own experience
                of the loneliness of unrequited love, with heartbroken
                sighs throughout. Love is framed as a pursuit, or a game,
                something to be sought, and although the voice of ‘Alone’
                is unashamedly male and egotistical, with the focus
                almost entirely on his own feelings, his intentions appear
                honourable. He speaks of the joy that he would feel if his
                feelings were returned:</p>
            <p><b>Once me to love if she began,<br>
                    No man with tongue nor pen tell can<br>
                    The joy in me that would be than.</b></p>


