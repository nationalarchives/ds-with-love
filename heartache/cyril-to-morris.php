            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>My Darling Morris,</p>
                    <p>Just a note which I feel I must write or should I say type to
                        you. I was very very disappointed to find out that you were
                        not coming to the Club to-night, as ever since I phoned
                        you on Monday and made arrangements, I just lived for
                        to-night when I was to have seen you darling, as a matter
                        of fact I stayed in bed all day yesterday, didn’t even get up
                        to eat and just thought of you, and counting the hours until
                        I should see you, and then the terrible shock, of not being
                        with you after all, I had a terrible feeling that I should be
                        disappointed so that is the reason I telephoned.</p>
                    <p>I always feel so embarrassed when we meet as the terrible
                        thought of your trouble keeps ringing in my ears, and I hate
                        myself and cried for hours when I lay and thought it all,
                        especially when I love you, Morris darling, honestly I do,
                        it is two years almost since we first met, then I was crazy
                        about you, but you just seemed to disappear and I never
                        saw you for months and I tried to forget, and now since
                        seeing you, it has all started again, and I just had to let you
                        know how I felt, please do not think I am foolish Morris but
                        I Love You such a lot, so please if you do not feel this way
                        Heartache to me, promise that we shall always be the best of friends, I
                        know it is very difficult to think the same of me since what
                        happened, even altho’ I told you what the Doctor said, however I hope you will forgive and
                        forget.</p>
                    <p>Well Morris darling I must ring off now and pray that I may
                        see you on Saturday.</p>
                    <p>I only wish that I was going away with you, just you and I
                        to eat sleep and make love together, perhaps when you are
                        away you will think of me sometimes and even write me, I,
                        sincerely hope so.</p>
                    <p>Well Morris darling until we meet,</p>
                    <p>I am Yours with all my love for ever and always,</p>
