            <h3>Read the transcript</h3>
            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>How happy was I, dear Olive, when I lived at home, secure
                        of your love, and never did suspect that anything could
                        have made you forget me. But now I see your memory
                        fails and my misfortune increases, and I fear that absence
                        hath made you neglect writing to me, and changed that
                        constant love which in my opinion was wholly mine. But
                        it may be that I lived deceived then, and God hath been
                        pleased with this occasion to open my eyes that you may
                        see how little you esteem me. Here have come two posts
                        and I have received no letters from you. It may be mine
                        have been so long, that because I should not trouble you
                        with so much letter you thought good to forbear writing,
                        thinking I could not be so shameless as to do it
                        without correspondence.</p>
                    <p>Alas! Sweet Olive, if my love were like yours I could forbear
                        to show it, but ‘tis impossible, for if you did but know how
                        miserably I pass this life from the sight of your eyes, you
                        could not choose but pity me.</p>
                    <p> I left my heart within your sweet breast at my departing
                        from you, and am united there with you in spite of this
                        tedious intermission of my joy, which makes me live here
                        like a man without a soul, therefore you ought to love that
                        love which is in me, though you have none yourself. Let
                        me entreat you to have a care to let me know how you
                        and your children do, though you write not me, for that is
                        some comfort and makes me enjoy myself a little. I wonder
                        my mother would forget me, but sure she knew not of the
                        post's coming.</p>
                    <p>I sent you by Dick Grimes a chain of gold which is of the
                        prettiest making that ever I saw. I pray you wear it and
                        let nobody know how kind I am to you, lest they laugh at
                        me for my fondness. By Killgree I sent you a feather, but I
                        fear I shall trouble you with tokens as I do with letters. Yet
                        I would willingly have nobody come without some small
                        remembrance to you, which makes me send you this poor
                        token now. I want a better, but cannot tell where to have
                        anything rich enough for my desire, which could not be
                        satisfied though I were powerful to send you the King of
                        Spain's wealth.</p>
                    <p>You never send me word how my lady doth. I pray God
                        send her much happiness, howsoever. I hope to see her
                        mother of many children.</p>
                    <p>Our business here is not likely to hold. We are to come
                        home suddenly, which I desire for nothing but to see you.
                        And as I was writing this the Prince concluded the business
                        himself with the King, so that it is now finished, and I hope
                        we shall all receive a great deal of comfort in it, for sure
                        there was never a better creature than the Infanta is. He
                        [the Prince] is to be contracted presently, and then means
                        to go away hence within these three weeks so that we shall
                        be at home suddenly. She is to be delivered in March next.
                        God be praised for so great a blessing as we shall all receive
                        by it. And, sweet Olive, have a care to love me as you have
                        ever professed, it may be I may deserve it; although you
                        think to the contrary, I am unwilling to trouble you with
                        long letters, although I have much ado to forbear.</p>
                    <p>The lady which writes to you is wife to a gentleman that
                        I served, I am much beholden to them. She sends you a
                        token, and I would have you write to her and send her some
                        small toy from thence. I have written the letter and copied
                        hers, for it is so ill a letter that nobody could read it. My
                        mother will help you understand them both.</p>
                    <p>[OVERLEAF]</p>
                    <p>And thus with my prayers to Almighty God for your health
                        and your children whose blessing and mine light upon
                        them. I kiss thy sweet mouth a thousand times and rest</p>
                    <p>Thy true loving husband,</p>
                    <p>Endymion Porter</p>
