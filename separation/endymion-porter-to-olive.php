            <h2>Why is this in The National Archives?</h2>
            <p>The State Papers held by The National Archives include about 30 letters
                from Endymion to Olive Porter and two from her to him. Although surviving
                letters between husbands and wives are not unusual from the 1600s, it
                is unusual to find them in the State Papers collection. They are possibly there
                because they were seized by the Parliamentary forces at the beginning of
                the Civil Wars.</p>
