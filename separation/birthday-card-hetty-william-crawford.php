                    <p>No 8 Wrenches Court<br>
                        Tarvin Road<br>
                        Chester<br>
                        29th January 1917</p>
                    <p>My Dear Will</p>
                    <p>I now take great pleasure of answering your kind and loving
                        letter. Well dear Will I am glad that you are keeping quite
                        well the same as I am at present, except another cold.
                        It nearly starves you to death at Queensferry. I shall be glad
                        when the weather gets warmer, it is a hard struggle to get
                        out of bed these cold mornings but I am afraid I should
                        never get out at all if you were there. Well dear Will I am
                        glad that you love me.</p>
                    <p>[OVERLEAF]</p>
                    <p>I want your love because I love you. I often think of you
                        and all that passed between us. But never mind dear Will
                        I am not sorry. I only wish that you were here now. I often
                        long for you. I should love to see you. I don’t know when I
                        shall see you again but I do hope that it will be very soon.
                        Well dear Will I think it was very good of you to say that
                        you couldn’t do as you like without thinking of me first
                        but you could dear Will because you know I have no claim
                        upon you. But still I shouldn’t like to think that what passed
                        between you and I would pass between you and someone else.</p>
                    <p>But I trust you dear Will. Well Will my brother has gone
                        back to France on Friday night. I had a letter from him
                        today he landed at Folkestone quite safely and I hope that
                        he will be spared to come back of course we know that
                        many will have to give their lives yet before this war will
                        end. But my one prayer is that they will never take you I
                        should be brokenhearted if they were</p>
                    <p>[OVERLEAF]</p>
                     <p>to take you. Well dear Will I don’t think there is much more
                        to tell you this time. Now be a good lad and I will be a good
                        girl. You won’t half have to love me when you come over
                        again Will [we’ll] have to make up for lost time dear Will.
                        Write soon. Will now draw to a close with heaps of love
                         and kisses.</p>
                     <p>Yours Hetty</p>
