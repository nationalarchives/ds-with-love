            <h2>Why is this in The National Archives?</h2>
            <p>Hetty’s letters can be found within William’s service record, which is unusual
                as personal possessions were generally returned to the families of soldiers
                who died in battle. The only family William had was an uncle by marriage,
                who in another letter asked that William’s medals and awards, along with his
                personal effects, be presented to the King in memory of him. It is likely they
                were returned to his regiment, the Household Battalion, to be destroyed.
                Instead they survived as a rare record of the loving relationship of a young
                couple unhappily separated by war.</p>
