            <h3>Read the transcript</h3>
            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>Praise God
                        <p>A friendly greeting being written to you my most beloved
                        and most worthy husband Willem Luckassen, I your wife
                        Meynneutien Meyderts am letting you know that I and your
                        little son Luckas Willemssen are in a good disposition and
                        in good health and I hope to hear that it is the same for
                        you, my dear husband, which all-knowing God knows.</p>
                        <p>Furthermore let me tell you, my beloved husband, that I
                        have received your letter and understand that you remain
                        at the Cape, having arrived safely – which has made me
                        very glad, as you can well imagine. And you write to me
                        that at night in your bunk you hold your pillow in your arms
                        and imagine that I am with you, my beloved, and that I
                        should write to you that I do the same – but it is in vain: I
                        must bear the time [of your return] with endurance, once
                        again. If the Lord will but spare your health, my beloved,
                        you are blest, Amen.</p>
                        <p>Further, let me tell you that which you need to know –
                        how things stand in our dear country, how it has been
                        wretchedly betrayed, which is a misery to write, but this I
                        will write. To put it briefly, the English and French have both
                        made war on us, but that is not all. Our country has been
                        sold to the French, and through that heinous and great
                        treason the most part has been lost to the French in a short
                        time. All would have been lost, had that not saddened
                        God, but now it has come to light you may imagine how it
                        stands here. For that reason the common people got two of
                        the leaders out of the way – yes, so far out of the way that
                        they are put to death, and in such a way that I cannot tell
                        you how they were torn and pulled apart and hacked
                        to pieces.</p>
                    <p>[OVERLEAF]</p>
                        <p>How it shall turn out with the others, only time will tell.
                        It has, praise God, come so far that the Prince [of Orange,
                        William III] is reluctantly made Stadtholder, and thank
                        God he will have no difficulty, because he has now got the
                        senior rulers to help – that is, the [Holy Roman] Emperor
                        and Brandenburg, with all the other princes. This is now all
                        kept as covert as the treason was previously.</p>
                        <p>If I write everything to you I would need a ream of paper,
                        therefore I will break off until further opportunity. The Lord
                        bring you prosperity wherever you are and go and write
                        to me at every opportunity wherever you are and shall go
                        to, and everything that happens, Amen. No more at this
                        moment than to send love from me your wife and son and
                        that my friends and your friends are all in good health.
                        Wishing your voyage may remain blest, Amen. Say to your
                        mate the chief boatswain many ‘good nights’ from his
                        wife and daughters, and also many to Eldert Janssen the
                        carpenter, as they are all still in good health.</p>
                        <p>[Unsigned]</p>
