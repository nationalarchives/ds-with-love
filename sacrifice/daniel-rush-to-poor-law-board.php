            <h3>Read the transcript</h3>
            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>Gentlemen</p>
                    <p>I have taken the Liberty of Writing to you Implorin of you
                    to take my Case into your most seirous Considration I am
                    a Silk Weaver Past Labour being 71 years of age my Wife
                    68 We Now Wind Silk and Cotton and When full employd
                    Can Earn from 4s to 5s per Week but trade being bad We
                    now earn abought 3s and I have aplyed to the Parish of St
                    Mathews bethnal Green and they Will not Relive me With
                    out my goin in to the Poor house and We went on Tuesday
                    the 19th and they insisted in Sepratin me from my Wife
                    Wich I have had 49 years or turn us out, and soner then
                    We Would be seperated We Will Perish for Want som time
                    a goe I had a Blad Vessel burst in my Leg and Went to the
                    Poor house and as soon as I got Well I took my Discharge
                    Mr Bresto the Reliving Officer met me in the yard and
                    told me to goe to Norwich and Claim my relatives
                        Property wich</p>
                    <p>[OVERLEAF]</p>
                    <p>[I] did not understand and he told he Ment Rush the
                        Murderer Wich Whas not any relative to me is not this
                        unbecomin to any man in his situation to trie to ereitate
                        any Person be Cause their Name is the same. I was once
                        Cast away in Africa in the Caffers Country and Traveled
                        1000 Miles and Found more Uemanity among thies Caffers
                        and Hottintots than I find a Mong thies Jacks in office
                        Gentlemen in the Act for the Administration of the Laws in
                        England 23rd July 1847 Chap 109 Verse 23 any two Persons
                        being Husband and Wife shall not be ness be Compeld to
                        be separate and Gentlmen I hope you will take my Case
                        into your Most seirous Consideration to alow me som little
                        Relief or not be separated in the Poor house</p>
                        <p>and I in Duty Bound<br>
                        Would Ever Pray<br>
                        Daniel Rush<br>
                        No.8 Littel George<br>
                        Street bethnal Green<br>
                        No Relative to Bloomfield Rush</p>
