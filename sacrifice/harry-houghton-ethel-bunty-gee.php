            <h1 class="text-center">Letter from Harry Houghton to Ethel 'Bunty' Gee</h1>
            <img class="letter-banner" src="../images/sacrifice/index-sacrifice-three.jpg" alt="">
            <p>The five members of the Portland Spy Ring were prosecuted
                and imprisoned in March 1961 for passing naval secrets
                to the Soviet Union. The spy ring took its name from
                the Underwater Weapons Establishment in Dorset, the
                workplace of two of the spies, Harry Houghton and his
                lover Ethel 'Bunty' Gee.</p>
            <p>Ethel consistently maintained that her only motivation
                was love for Harry. Letters they exchanged whilst both
                incarcerated demonstrate their affection for each other.
                Here Harry writes to console his "sweetheart"
                Ethel following the death of her mother, expressing his
                sympathy in fulsome terms.</p>
            <p>They were released in 1970 and married the following year.</p>

