                    <p>H.M Prison<br>
                        Romsey Road<br>
                        Winchester<br>
                        Hants</p>
                    <p>24th February 1966</p>
                    <p>My dear Darling Bunty,</p>
                    <p>I cannot say how grieved I am at the news about your
                        mum received during the mid-day break. My thoughts are
                        with you more than ever during this terrible period of your
                        imprisonment and they also go out to Uncle Jack. I do hope
                        this will not cause him to break up under all the strain he
                        has had to endure in the last five years.</p>
                    <p>I feel very bitter about it all, her death could have been
                        avoided, you know this as well as I. Its no use asking
                        foreigners to show clemency when we do not practice it
                        in this country.</p>
                     <p>Poor old mum, she was so kind to me in many ways and
                         I will always remember how she wore herself in a frazzle
                         looking after Auntie Bess.</p>
                        <p>Try to cheer up darling under this terrible strain, she was
                            a very lucky woman to have had such a devoted daughter
                            who considered her at the expense of her own life.</p>
                    <p>[OVERLEAF]</p>
                    <p>I appreciate how helpless you feel and the hopelessness of
                        your positon, weâ€™ve known all along this could happen at
                        any time but now it has come I know how terrible you must
                        be feeling judging by own thoughts and I do want you to
                        know how I am with you all the time in your distress.</p>
                    <p>Try to let me know Sweetheart the time, day and place
                        of the funeral, I want to join in with you in my thoughts. I
                        should imagine you will be able to attend the funeral and
                        I am very sorry that you were not permitted to see her
                        before the end came, I thought perhaps this would have
                        been granted. No doubt there will be an inquest.</p>
                    <p>I do not think I ought to write to your Uncle it may cause
                        him more distress but if you ever get the chance please let
                        him know I grieve for him in his hour of trouble.</p>
                    <p>Do not let anyone tell you it happened for the best, it
                        certainly did not, she was waiting for you.</p>
                    <p>An announcement was made on the regional wireless & TV
                        about her tonight.</p>
                    <p>Darling, all I can say is that I am sorry and this comes from
                        the bottom of my heart. I wish I could say more to you & to
                        comfort you but I am certain you will understand.</p>
                    <p>All my love sweetheart, you all are very much in my
                        thoughts.</p>
                    <p>Yours ever,</p>
                    <p>Harry x x x</p>
