                    <p>My Dear One</p>
                    <p>You have no idea (I am sure you have not) of how queer
                        the world was with you out of it. You are such a nuisance
                        when you are in, and such a trouble when you are out. You
                        have no place in the ordered universe but fly about to make
                        the cold planets waggle in their courses. But now you are
                        back things become a little homely again. At the outset,
                        however, let me suppress a hope that you are not now bent
                        on marrying me. I would be a very cruel & hard husband
                        guiding you with a heavy hand and using the snaffle
                        habitually. If I were sure you still stuck to your principles
                        I would pitch another tale. But that will wait till I see. Tell
                        me all about you - your body, your soul, your heart. Do you
                        want to kiss me, do you want to come into the jungle? Just
                        explain yourself, for you are such a stranger. Why aren’t you
                        here? I want you and you are hundreds of miles away and
                        I cannot even kiss you. Then you send me invitations that
                        are impossible. Events are whizzing ahead and today I have
                        been summoned</p>
                    <p>[OVERLEAF]</p>
                    <p>by wire to attend a meeting in London. Henderson does
                        not want to give up his money as a member of the cabinet
                        and so wants the party to agree to conscription – so I am
                        told – and meetings are being summoned. I fear it means I
                        will fly through. But then I have meetings fixed in Scotland
                        and hospitality arranged (when you went away). I have
                        arranged with Whiteley’s to move us the day after I speak in
                        Stirling and must travel down by the night train not going
                        through Edinburgh in consequence. So I fear, whatever
                        happens, we are not to set eyes on each other this time.
                        Some devilish wit of creation is grinning in consequence.
                        My next may give more particulars, but meanwhile I kiss
                        and embrace you long and closely and ever so sweetly,
                        my dear</p>
                    <p>R.</p>
                    <p>[He added a postscript:]</p>
                    <p>Your wire has come</p>
