            <h3>Read the transcript</h3>
            <div class="row mb-5">
                <div class="col-md-12 m-3 transcript-panel">
                    <p>I most humbly beseech your Majesty to pardon your poor
                        old servant to be thus bold in sending to know how my
                        gracious lady doth, and what ease of her late pain she finds,
                        being the chiefest thing in this world I do pray for, for her
                        to have good health and long life. For my own poor case,
                        I continue still your medicine and find that (it) amends
                        much better than with any other thing that hath been given
                        me. Thus hoping to find perfect cure at the bath, with the
                        continuance of my wonted prayer for your Majesty’s most
                        happy preservation, I humbly kiss your foot. From your old
                        lodging at Rycote, this Thursday morning, ready to take
                        on my Journey, by your Majesty’s most faithful and
                        obedient servant,</p>
                    <p>R. Leicester</p>
                    <p>[He added a postscript:]</p>
                    <p>Even as I had writ thus much, I received Your Majesty’s
                        token by Young Tracey.</p>
