            <p>Elizabeth I's affection for her childhood friend Robert
                Dudley was a constant throughout her reign, and at times
                her intimacy with a married man gave rise to gossip,
                threatening her reputation as a chaste and virtuous queen.
                Choosing to maintain this reputation and the balance of
                political power, she decided against marrying him after the
                death of his first wife, Amy Dudley (née Robsart). Initially
                furious at his second marriage, by the time of this letter
                and his death they were again close friends.</p>
                <p>This was his final letter to her, and his affection for his
                friend and ruler is clear – though somewhat coded in places.
                He’s drawn eyebrow-like dashes above the double-os in
                "pōōr" [the last word of the first line] to mimic her nickname
                for him – 'Eyes'.</p>


